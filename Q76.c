//PROGRAM USING WHILE LOOP TO READ MULTIPLE NUMBERS FROM STANDARD INPUT AND PRINTS THEIR SUM WHEN SUM EXEEDS HUNDRED

//INPUT

#include <stdio.h>
void main() {
  int total = 0;
  while (total = 0);
         while (total <= 100) {
           int num;
           printf("Enter a number : ");
           scanf("%d", &num);
           total += num;
         }
         printf("The total of given numbers is : %d\n", total);
 }
         
//OUTPUT
//Enter a number : 89
//Enter a number : 45
//The total of given numbers is : 134
